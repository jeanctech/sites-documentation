            Un identificador de JavaScript debe comenzar con una letra, un
            guión bajo (_) o un signo de dólar ($); los caracteres
            subsiguientes también pueden ser dígitos (0-9). Dado que
            JavaScript distingue entre mayúsculas y minúsculas, las letras
            incluyen los caracteres de la "A" a la "Z" (mayúsculas) y los
            caracteres de la "a" a la "z" (minúsculas).
          </p>
          <p class="main-p">
            Puede usar ISO 8859-1 o letras Unicode como å y ü en los
            identificadores. También puede usar las secuencias de escape
            Unicode como caracteres en los identificadores. Algunos ejemplos
            de nombres legales son Number_hits, temp99 y _name.
          </p>
