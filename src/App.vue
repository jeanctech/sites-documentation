      <section class="main-section" id="if...else-declaraciones">
        <header class="main-header">
          <p><strong>if...else Declaraciones</strong></p>
        </header>
        <article class="main-article">
          <p class="main-p">
            Use la declaración if para ejecutar una declaración si una
            condición lógica es verdadera. Use la cláusula else opcional para
            ejecutar una declaración si la condición es falsa. Una sentencia
            if tiene el siguiente aspecto:
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              if (condition) { statement_1; } else { statement_2; }
            </pre>
          <p class="main-p">
            condición puede ser cualquier expresión que se evalúe como
            verdadera o falsa. Consulte Boolean para obtener una explicación
            de lo que se evalúa como verdadero y falso.
          </p>
          <p class="main-p">
            Si la condición se evalúa como verdadera, se ejecuta la
            declaración_1; de lo contrario, se ejecuta la declaración_2.
            declaración_1 y declaración_2 pueden ser cualquier declaración,
            incluidas otras declaraciones if anidadas.
          </p>
          <p class="main-p">
            También puede combinar las declaraciones usando else if para
            probar múltiples condiciones en secuencia, de la siguiente manera:
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              if (condition_1) { statement_1; }
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              else if (condition_2)
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              { statement_2; }
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              else if (condition_n) { statement_n; }
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              else {statement_last; }
            </pre>
          <p class="main-p">
            En el caso de varias condiciones, solo se ejecutará la primera
            condición lógica que se evalúe como verdadera. Para ejecutar
            varias sentencias, agrúpelas dentro de una sentencia de bloque ({
            ... }) . En general, es una buena práctica usar siempre sentencias
            de bloque, especialmente al anidar sentencias if:
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              if (condition) { statement_1_runs_if_condition_is_true;
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              statement_2_runs_if_condition_is_true; } else {
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              statement_3_runs_if_condition_is_false;
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              statement_4_runs_if_condition_is_false; }
            </pre>
          <p class="main-p">
            Es aconsejable no utilizar asignaciones simples en una expresión
            condicional, porque la asignación puede confundirse con la
            igualdad al revisar el código. Por ejemplo, no use el siguiente
            código:
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              if (x = y) { /* statements here */ }
            </pre>
          <p class="main-p">
            Si necesita usar una tarea en una expresión condicional, una
            práctica común es colocar paréntesis adicionales alrededor de la
            tarea. Por ejemplo:
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              >if ((x = y)) { /* statements here */ }
            </pre>
        </article>
