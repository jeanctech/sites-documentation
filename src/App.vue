              cliente permiten que una aplicación coloque elementos en un
              formulario Html y responder a eventos de usuario como el mouse
              clics, entrada de formularios y navegación de páginas.
            </li>
            <br>
            <li>
              JavaScript del lado del servidor amplía el lenguaje principal al
              proporcionar objetos relevantes para ejecutar JavaScript en un
              servidor. Por ejemplo Las extensiones del lado servidor permiten
              que una aplicación se comunique con un base de datos,
              proporcionar continuidad de la información desde una invocación
