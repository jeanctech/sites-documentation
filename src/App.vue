      <section class="main-section" id="while_sentencias">
        <header class="main-header">
          <p><strong>While Sentencias</strong></p>
        </header>
        <article class="main-article">
          <p class="main-p">
            Una declaración while ejecuta sus declaraciones siempre que una
            condición específica se evalúe como verdadera. Una instrucción
            while tiene el siguiente aspecto:
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              while (condition) statement
            </pre>
          <p class="main-p">
            Si la condición se vuelve falsa, la instrucción dentro del bucle
            deja de ejecutarse y el control pasa a la instrucción que sigue al
            bucle.
          </p>
          <p class="main-p">
            La prueba de condición ocurre antes de que se ejecute la
            declaración en el ciclo. Si la condición devuelve verdadero, la
            declaración se ejecuta y la condición se prueba nuevamente. Si la
            condición devuelve falso, la ejecución se detiene y el control se
            pasa a la instrucción que sigue a while.
          </p>
          <p class="main-p">
            Para ejecutar varias declaraciones, use una declaración de bloque
            ({ ... }) para agrupar esas declaraciones.
          </p>
          <br />
          <strong>Ejemplo:</strong>
          <p class="main-p">
            El siguiente bucle while itera siempre que n sea menor que tres:
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">var n = 0; var x = 0; while (n &lt; 3) { n++; x += n; }
            </pre>
          <p class="main-p">
            Con cada iteración, el ciclo incrementa n y agrega ese valor a x.
            Por lo tanto, x y n toman los siguientes valores:
          </p>
          <ul class="main-ul">
            <li>Después del Primer Pase: n = 1 and x = 1</li>
            <li>Después del Segundo Pase: n = 2 and x = 3</li>
            <li>Después del Tercer Pase: n = 3 and x = 6</li>
          </ul>
          <p class="main-p">
            Después de completar el tercer paso, la condición n &lt;3 ya no es
            verdadera, por lo que el ciclo termina.
          </p>
        </article>
      </section>
      <section class="main-section" id="declaraciones-de-funciones">
        <header class="main-header">
          <p><strong>Declaraciones de Funciones</strong></p>
        </header>
        <article class="main-article">
          <p class="main-p">
            Una definición de función (también llamada declaración de función
            o declaración de función) consta de la palabra clave de función,
            seguida de:
          </p>
          <ul class="main-ul">
            <li>El nombre de la función.</li>
            <li>
              Una lista de argumentos para la función, entre paréntesis y
              separados por comas.
            </li>
            <li>
              Las declaraciones de JavaScript que definen la función,
              encerradas entre corchetes, { }.
            </li>
          </ul>
          <p class="main-p">
            Por ejemplo, el siguiente código define una función simple llamada
            cuadrado:
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              function square(number) { return number * number; }
            </pre>
          <p class="main-p">
            El cuadrado de la función toma un argumento, llamado número. La
            función consta de una declaración que dice devolver el argumento
            de la función (es decir, el número) multiplicado por sí mismo. La
            declaración de retorno especifica el valor devuelto por la
            función.
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              return number * number;
            </pre>
          <p class="main-p">
            Los parámetros primitivos (como un número) se pasan a funciones
            por valor; el valor se pasa a la función, pero si la función
            cambia el valor del parámetro, este cambio no se refleja
            globalmente ni en la función que llama.
          </p>
        </article>
