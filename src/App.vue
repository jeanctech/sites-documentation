      <section class="main-section" id="ambitos-de-variables">
        <header class="main-header">
          <p><strong>Ambitos de Variables</strong></p>
        </header>
        <article class="main-article">
          <p class="main-p">
            Cuando declara una variable fuera de cualquier función, se
            denomina variable global, porque está disponible para cualquier
            otro código en el documento actual. Cuando declara una variable
            dentro de una función, se denomina variable local, porque solo
            está disponible dentro de esa función.
          </p>
          <p class="main-p">
            JavaScript anterior a ECMAScript 2015 no tiene alcance de
            declaración de bloque; más bien, una variable declarada dentro de
            un bloque es local a la función (o ámbito global) en el que reside
            el bloque. Por ejemplo, el siguiente código registrará 5, porque
            el alcance de x es la función (o contexto global) dentro del cual
            se declara x, no el bloque, que en este caso es una declaración
            if.
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              if (true) { var x = 5; } console.log(x); // 5
            </pre>
          <p class="main-p">
            Este comportamiento cambia cuando se usa la declaración let
            introducida en EcmaScript 2015.
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              if (true) { let y = 5; } console.log(y); // ReferenceError: y is not defined
            </pre>
        </article>
