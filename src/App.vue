          <p><strong>Constantes</strong></p>
        </header>
        <article class="main-article">
          <p class="main-p">
            Puede crear una constante con nombre de solo lectura con la
            palabra clave const. La sintaxis de un identificador constante es
            la misma que la de un identificador variable: debe comenzar con
            una letra, un guión bajo o un signo de dólar y puede contener
            caracteres alfabéticos, numéricos o guiones bajos.
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              const Pi = 3.14;
            </pre>
          <p class="main-p">
            Una constante no puede cambiar el valor a través de la asignación
            ni volver a declararse mientras se ejecuta el script. Tiene que
            ser inicializado a un valor.
          </p>
          <p class="main-p">
            Las reglas de ámbito para las constantes son las mismas que para
            las variables de ámbito de bloque let. Si se omite la palabra
            clave const, se supone que el identificador representa una
            variable.
          </p>
          <p class="main-p">
            No puede declarar una constante con el mismo nombre que una
            función o variable en el mismo ámbito. Por ejemplo:
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              // This Will Cause An Error function
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              f() {}; const f = 5;
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              // This Will Cause An Errow Also function
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              f() { const g = 5; }
            </pre>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              f() { const g = 5; var g; //statements }
            </pre>
          <p class="main-p">
            Sin embargo, los atributos del objeto no están protegidos, por lo
            que la siguiente instrucción se ejecuta sin problemas.
          </p>
          <pre id="main-pre-row" class="prettyprint lang-javascript">
              const MY_OBJECT = {"key": "value"}; MY_OBJECT.key = "otherValue";
            </pre>
