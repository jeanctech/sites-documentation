      <section class="main-section" id="javaScript-y-java">
        <header class="main-header">
          <h4><strong>JavaScript y Java</strong></h4>
        </header>
        <article class="main-article">
          <p class="main-p">
            JavaScript y Java son similares en algunos aspectos, pero
            fundamentalmente diferentes en otros. El lenguaje JavaScript se
            parece a Java, pero no tiene el tipado estático ni la verificación
            de tipos fuerte de Java. JavaScript sigue la mayoría de las
            sintaxis de expresiones de Java, las convenciones de nomenclatura
            y las construcciones básicas de flujo de control, razón por la
            cual se cambió el nombre de LiveScript a JavaScript.
          </p>
          <p class="main-p">
            A diferencia del sistema de clases en tiempo de compilación de
            Java construido por declaraciones, JavaScript admite un sistema de
            tiempo de ejecución basado en una pequeña cantidad de tipos de
            datos que representan valores numéricos, booleanos y de cadena.
            JavaScript tiene un modelo de objetos basado en prototipos en
            lugar del modelo de objetos basado en clases más común. El modelo
            basado en prototipos proporciona una herencia dinámica; es decir,
            lo que se hereda puede variar para objetos individuales.
            JavaScript también admite funciones sin ningún requisito
            declarativo especial. Las funciones pueden ser propiedades de
            objetos, ejecutándose como métodos vagamente tipificados.
          </p>
          <p class="main-p">
            JavaScript es un lenguaje de forma muy libre en comparación con
            Java. No tiene que declarar todas las variables, clases y métodos.
            No tiene que preocuparse por si los métodos son públicos, privados
            o protegidos, y no tiene que implementar interfaces. Las
            variables, los parámetros y los tipos de devolución de funciones
            no se escriben explícitamente.
          </p>
        </article>
